######################################################################################
# Usage: ./simpleRecon -i/--ip [IP]
# Usage: ./simpleRecon -i/--ip scanme.nmap.org

while [ "$1" != "" ]; do
    case "$1" in
    -i | --ip)
        ip="$2"
        shift
        ;;
    esac
    shift
done

# handling input error
if [[ $ip == "" ]]; then
    echo
    echo "Usage: ./simpleRecon -i/--ip [IP]"
    echo "Usage: ./simpleRecon -i/--ip scanme.nmap.org"
    exit
fi

# performing scan
echo "================================="
echo "Scanning: $ip"
echo "================================="
for i in {1..1024}; do
    (echo >/dev/tcp/$ip/$i) >/dev/null 2>&1 && echo "Port $i is open"
done
echo "================================="
echo "Scan complete."
echo "================================="
